void main() {

  // arithmetic operators
  int a = 10, b = 3;
  int result = a + b;

  print(result);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);
  print((a + b) * (a - b));
  print('\n');

  // logical operators
  bool c = true;
  bool d = false;

  print(c && d);
  print(c || d);
  print(c ^ d);
  print(!c);
  print(!!d);
}